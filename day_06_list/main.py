shopping_list = ["Apple","Banana","Milk","Bread"]

shopping_list.append("Pulses") # Used to add a string
print(shopping_list)

shopping_list.remove("Apple")  # Used to remove a specific string from the list
print(shopping_list)

print(shopping_list[1])  # We can  use indecies to display specific items
print(shopping_list[0:3])  # Slicing can also be used on lists

shopping_list.sort()  # Used to sort the list
print(shopping_list)